//initialize rows to 1
//starting rows from 1 and increasing to 4
for (var rows = 1; rows <= 4; rows++) {

    //declaring var cols and assigning to 1 and increasing upto 4
    for (var cols = 1; cols <= rows; cols++) {
        document.write(rows + "&emsp;");
    }
    document.write("<br><br>");
}